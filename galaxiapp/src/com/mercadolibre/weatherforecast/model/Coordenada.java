package com.mercadolibre.weatherforecast.model;

public class Coordenada {

	private final double x;
	private final double y;

	public Coordenada(double x, double y) {
		this.x = x;
		this.y = y;
	}

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}
}
